    <h2 class="pt-6 mb-4-mobile  has-text-centered">¿Cómo avanza la distribución y aplicación de vacunas en la Argentina?</h2>     
    <p class="has-text-centered">
      Cuando un avión con vacunas aterriza en Ezeiza, se ponen en marcha una serie de tareas logísticas. En primer lugar, se realiza el trámite aduanero. Luego, se revisa cada lote, se verifica que la documentación sea la correcta y que el laboratorio de origen está aprobado. Finalmente, se hace el control térmico, conteo, fraccionamiento y acondicionamiento que asegura la calidad y la buena conservación de los antígenos. Recién ahí las dosis están listas para la distribución.
